  window.showPillExpansion = function(btn, topic) {
    // הסרת הרחבות קודמות
    document.querySelectorAll('.pill-expanded').forEach(el => el.remove());
    
    // בדיקה אם כבר יש הרחבה
    if (btn.nextElementSibling && btn.nextElementSibling.classList.contains('pill-expanded')) {
      btn.nextElementSibling.remove();
      return;
    }
    
    // טיפול מיוחד בכפתורי tracks ו-regulations - הצגת תפריט
    if (topic === 'tracks') {
      showTracksMenu();
      return;
    }
    if (topic === 'regulations') {
      showRegulationsMenu();
      return;
    }
    
    // בדיקה שהנושא קיים
    if (!topic || !topicQuestions[topic]) {
      console.warn(`נושא לא נמצא: ${topic}`);
      return;
    }
    
    const questions = (topicQuestions[topic] || []).slice(0, 2); // רק 2 שאלות
    if (questions.length === 0) {
      console.warn(`אין שאלות לנושא: ${topic}`);
      return;
    }
    
    const expansion = document.createElement('div');
    expansion.className = 'pill-expanded';
    
    const questionsDiv = document.createElement('div');
    questionsDiv.className = 'pill-questions';
    
    questions.forEach(q => {
      const questionBtn = document.createElement('button');
      questionBtn.className = 'pill-question';
      questionBtn.textContent = '❓ ' + q;
      questionBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        const userInput = input;
        userInput.value = q;
        userInput.focus();
        // לא שולחים אוטומטית - המשתמש צריך ללחוץ על שלח
        expansion.remove();
        // גלילה לשדה הקלט
        setTimeout(() => {
          userInput.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
        }, 100);
      });
      questionsDiv.appendChild(questionBtn);
    });
    
    expansion.appendChild(questionsDiv);
    btn.parentNode.insertBefore(expansion, btn.nextSibling);
    
    // גלילה להרחבה
    setTimeout(() => {
      expansion.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    }, 100);
  }

